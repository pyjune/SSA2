def f(n, s):  # 주사위 값, 지나간 칸의 합
    global maxV
    if n == 10:
        if maxV < s:
            maxV = s
    else:  # 남은 횟수가 있으면
        for k in range(4):  # 4개의 말을 각각 움직여 봄
            if pos[k] != 21:  # 도착이 아니면
                pre = pos[k]  # 원래 위치 저장
                e = mv[pos[k]][dice[n]]  # 이동한 칸
                if e==21 or (e!=21 and e not in pos):
                    pos[k] = e
                    f(n + 1, s + mv[e][0])
                pos[k] = pre

